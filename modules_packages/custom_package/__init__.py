"""__init__.py is a special file used in Python 
to define packages and initialize their namespaces."""

"""In Python, __init__.py is used to tell Python 
that a directory(folder) is a package.

Here's the simple explanation:

✅ Why is __init__.py required?
1. It makes Python treat the folder as a package

Before Python 3.3, a folder must have an __init__.py 
file to be considered a package. Without it, 
you couldn’t import modules from that folder.

Even though newer Python versions allow "namespace packages"
 without an __init__.py, it is still widely used."""


